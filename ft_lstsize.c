#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*l;
	int		i;

	if (!lst)
		return (0);
	l = lst;
	i = 0;
	while (l)
	{
		i++;
		l = l->next;
	}
	return (i);
}
